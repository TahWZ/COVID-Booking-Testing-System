    /**
     * Loads customer's bookings to the bookings table
     */
    private void loadBookings(){
        bookingCollection bCollection = bookingAPICollection.getInstance();
        Booking[] bookings = bCollection.getBookingsByUsername(currentUser.getUsername());
        DefaultTableModel model = (DefaultTableModel) bookingTable.getModel();
        model.setNumRows(0);
        if (bookings != null){
            for (Booking b: bookings) {
                try{
                    Object[] row = {
                        b.getID(),
                        b.getPatient().getFullName(),
                        b.getDateTime(),
                        b.getBookSite().getName(),
                        b.getTestType(),
                        b.getBookStatus(),
                        b.getLastModTime()
                    };
                    model.addRow(row);
                } catch (Exception e){
                }
            }
        }
        
    }
    
