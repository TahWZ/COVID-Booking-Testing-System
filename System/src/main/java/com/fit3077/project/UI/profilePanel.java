    /**
     * Validates the booking selected in the table
     */
    private Boolean validateTableSelection(){
        int row = bookingTable.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(this, "Please select a booking first", "Request failed",JOptionPane.INFORMATION_MESSAGE);
            return false;
        } 
        String bookingID = (String)bookingTable.getValueAt(row,0);
        bookingCollection bCollection = bookingAPICollection.getInstance();
        selectedBooking = bCollection.getBookingByID(bookingID);
        return checkBookingUser() && checkBooking();
    } 
    
    /**
     * Validates the booking entered in the search input field
     */
    private Boolean validateSearchInput(){
        if (bookingIDField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a booking ID first", "Request failed",JOptionPane.INFORMATION_MESSAGE);
            return false;
        } 
        bookingCollection bCollection = bookingAPICollection.getInstance();
        String bookingID = bookingIDField.getText();
        selectedBooking = bCollection.getBookingByID(bookingID);
        return checkBooking() && checkBookingUser();
    } 
   
    /**
     * Check if current logged in user is the owner of the booking
     */
    private Boolean checkBookingUser(){
         if (selectedBooking.getPatient().getUsername() == currentUser.getUsername()){
             return true;
         }
         JOptionPane.showMessageDialog(this, "Booking does not belong to current user", "Request failed",JOptionPane.INFORMATION_MESSAGE);
         return false;
    } 
    
    /**
     * Checks if the booking is valid for modifications
     */
    private Boolean checkBooking(){
         if (selectedBooking == null){
             JOptionPane.showMessageDialog(this, "Please enter a valid booking ID", "Request failed",JOptionPane.INFORMATION_MESSAGE);
             return false;
         } else if (selectedBooking.getBookStatus() == bookingStatus.LAPSED || selectedBooking.getBookStatus() == bookingStatus.COMPLETE){
             JOptionPane.showMessageDialog(this, "Lapsed/Completed bookings can't be modified", "Request failed",JOptionPane.INFORMATION_MESSAGE);
             return false;
         }
         return true;
    }
  
   /**
     * (bookingListener class method)
     * Called whenever a change to any bookings is detected.
     * Refreshes the booking table
     */
    public void refreshBookings(bookingNotification bNotification){
        loadBookings();
    }

